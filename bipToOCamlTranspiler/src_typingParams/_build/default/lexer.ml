  let next_token =
    let tokens = Queue.create () in (* next tokens to emit *)
    fun lb ->
      if Queue.is_empty tokens then begin
        let l = next_tokens lb in
        Queue.add l tokens
      end;
      Queue.pop tokens

  open Format

  let pp_token fmt (t: token) =
    match t with
    | WHILE -> fprintf fmt "while"
    | TIMES -> fprintf fmt "times"
    | THEN -> fprintf fmt "then"
    | SPEC_EQUAL -> fprintf fmt "<->"
    | RSQ -> fprintf fmt "]"
    | RP -> fprintf fmt ")"
    | RFLOOR -> fprintf fmt "_|"
    | REF -> fprintf fmt "ref"
    | PRINT -> fprintf fmt "print"
    | PLUS -> fprintf fmt "+"
    | PIPE -> fprintf fmt "|"
    | OR -> fprintf fmt "or"
    | NOT -> fprintf fmt "not"
    | MOD -> fprintf fmt "mod"
    | MINUS -> fprintf fmt "-"
    | LSQ -> fprintf fmt "["
    | LP -> fprintf fmt "("
    | LFLOOR -> fprintf fmt "|_"
    | LET -> fprintf fmt "let"
    | INT -> fprintf fmt "int"
    | NONE -> fprintf fmt "None"
    | IN -> fprintf fmt "in"
    | IF -> fprintf fmt "if"
    | IDENT s -> fprintf fmt "%s (identifier)" s
    | FOR -> fprintf fmt "for"
    | EQUAL -> fprintf fmt "="
    | EOF -> fprintf fmt "eof"
    | END -> fprintf fmt "end"
    | ELSE -> fprintf fmt "else"
    | DONE -> fprintf fmt "done"
    | DO -> fprintf fmt "do"
    | TO -> fprintf fmt "to"
    | DIV -> fprintf fmt "/"
    | DEREF -> fprintf fmt "!"
    | CST c ->
      let s =
        match c with
        | Cint  i -> string_of_int i
        | Cbool b -> string_of_bool b
        | Cstring str -> str
        | Cnone -> "Cnone"
      in
      fprintf fmt "%s (const)" s

    | COMMA -> fprintf fmt ","
    | COLON -> fprintf fmt ":"
    | SEMICOLON -> fprintf fmt ";"
    | CMP op ->
      let s =
        match op with
        | Beq  -> "=="
        | Bneq -> "!="
        | Blt  -> "<"
        | Ble  -> "<="
        | Bgt  -> ">"
        | Bge  -> ">="
        | _ -> assert false
      in
      fprintf fmt "%s (cmp)" s

    | BOOL -> fprintf fmt "bool"
    | BEGIN -> fprintf fmt "begin"
    | SET -> fprintf fmt ":="
    | AND -> fprintf fmt "and"

  let () =
    let fname = Sys.argv.(1) in
    let cin = open_in fname in
    let lb = Lexing.from_channel cin in
    let rec loop () =
      let token : Parser.token = next_token lb in
       eprintf "@[%a@]@." pp_token token; 
      if token <> EOF then loop () in
    loop ()


